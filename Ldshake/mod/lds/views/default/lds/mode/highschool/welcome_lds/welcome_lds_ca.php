<h2>La Barra superior</h2>

<hr />
<p style="text-align: justify;"><strong>La Barra superior est&agrave; dividida en cinc seccions principals:</strong></p>

<p style="text-align: justify;"><strong>Problemes:&nbsp;</strong> Aqu&iacute; trobar&agrave;s un llistat dels diferents problemes a treballar. Al fer un clic en un dels problemes es mostrar&agrave; l&#39;enunciat amb tota la informaci&oacute; necessaria.</p>

<p style="text-align: justify;"><strong>Proposar Soluci&oacute;:&nbsp;</strong> Un cop hem decidit quin problema treballarem venim a Solucions i seleccionem per quin problema volem fer una proposta. Al fer clic s&#39;ens obrir&agrave; una plantilla preparada per a treballar. Recordeu afegir un nom, i compartir-la amb els companys pertinents si fos necessari. Sobretot guardeu abans de sortir ( si no heu guardat el programa us ho recordar&agrave; ;D ).</p>

<p><strong>Explora</strong>: Dintre d&#39;aquesta secci&oacute; podrem accedir a dos subseccions:</p>

<ul>
    <li style="margin-left: 40px;"><strong>Les meves solucions: </strong>En aquesta secci&oacute; trobar&agrave;s totes les propostes de soluci&oacute; que hagis creat i totes les que hagin compartit amb tu amb drets d&#39;escriptura.</li>
</ul>

<ul>
    <li style="text-align: justify; margin-left: 40px;"><strong>Altres solucions:</strong> Aqu&iacute; trobar&agrave;s totes les propostes de soluci&oacute; de les quals tinguis drets de lectura.</li>
</ul>

<p style="text-align: justify;"><strong>LdShakers:</strong> Tots els estudiants i professors estan llistats en aquesta secci&oacute;. Aqu&iacute; pots administrar els teus grups d&#39;usuaris, amb els que podr&agrave;s compartir les teves propostes de soluci&oacute;. Visitant el perfil d&#39;un LdShaker et permetr&agrave; veure si aquest participant ha creat alguna proposta de soluci&oacute; que estigui compartint amb tu.</p>

