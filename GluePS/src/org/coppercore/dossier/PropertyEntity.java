package org.coppercore.dossier;

/**
 * Specifies the interface for the PropertyEntity bean.
 *
 * <p>This interface extends the EJBLocalObject by adding methods for setting
 * and getting members of the PropertyEntity instances.
 *
 * @author Harrie Martens
 * @author Hubert Vogten
 * @version $Revision: 1.9 $, $Date: 2007/04/04 09:04:07 $
 */
public interface PropertyEntity
    extends javax.ejb.EJBLocalObject {

  /**
   * Returns a data transfer object containing all data members of this instance.
   *
   * @return PropertyDto the data transfer object containing all data members
   *   of this instance
   */
  public PropertyDto getDto();

  /**
   * Sets the value of this property to the specified value.
   *
   * <p>PropertyEntity's are of a generic data type meaning that the value is
   * always stored as a String, or null.
   *
   * @param propValue String the new value of the property
   */
  public void setPropValue(String propValue);

}
